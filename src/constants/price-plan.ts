export type Plan = {
  id: string;
  createdAt: string;
  modifiedAt: string | null;
  trialInterval: string;
  trialIntervalCount: number;
  name: string;
  description: string;
  visibility: string;
  recurringInterval: string;
  recurringIntervalCount: number;
  isRecurring: boolean;
  isArchived: boolean;
  organizationId: string;
  metadata: object;
  prices: Price[];
  benefits: Benefit[];
  medias: never[];
  attachedCustomFields: never[];
  popular: boolean;
};

export interface Price {
  createdAt: string;
  modifiedAt: null;
  id: string;
  source: string;
  amountType: string;
  priceCurrency: string;
  taxBehavior: null;
  isArchived: boolean;
  productId: string;
  priceAmount: number;
}

export interface Benefit {
  id: string;
  createdAt: string;
  modifiedAt: null;
  type: string;
  description: string;
  selectable: boolean;
  deletable: boolean;
  organizationId: string;
  metadata: object | BenefitMetadata;
  properties: object;
}

export interface BenefitMetadata {
  pro?: string;
  free?: string;
  basic?: string;
}
